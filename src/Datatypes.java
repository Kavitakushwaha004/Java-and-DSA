class DataTypes {
    public static void main( String[] args) {
        byte a = 5; // size of byte is 1 byte
        short b = 100; //size of short is 2 byte
        int c = 1000; // size of int is 4 byte
        float d = 2.3f; //size of float is 4 byte and write f  for float data type
        char e = 'A'; // size of character is 2 byte
        boolean issafe = true;// boolean is a data type and its size is 1 byte
        long f = 2545; // long is data type whose size is 8 byte
        double g = 54754; // it is a data type whose size is 8 byte


        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(issafe);
        System.out.println(f);
        System.out.println(g);
    }
}

